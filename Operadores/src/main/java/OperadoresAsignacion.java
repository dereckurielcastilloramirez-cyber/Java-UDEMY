public class OperadoresAsignacion {
    public static void main(String[] args) {
        int i = 5; //asignacion
        int j = i + 3;
        //operadores combinados o compuestos
        
        i = i + 2;
        i += 2;
        
        j -= 2; // j -3
        
        j *= 5;
        
        j /= 2;

        System.out.println("j = " + j);
        
        String str = "Dereck Uriel ";
        str += "Castillo Ramirez";

        System.out.println("str = " + str);
    }
}
